public class Switch {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// can be a byte , short, int , or char
		int score = 90;
		switch (score )
		{
		case 90 : 
			System.out.println("Very Good");
			break; // break out of the switch statement
		case 60 :
			System.out.println("Good");
		
			break; // break out of the switch statement 
		case 30 :
			System.out.println("not Good");
			break; // break out of the switch statement
			
			
			
		}
		
	}

}
