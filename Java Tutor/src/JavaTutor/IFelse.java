package JavaTutor;

public class IFelse {

	/*
	 * == is equal to != is not equal to > is greater than < is less than >= is
	 * greater than or equal to <= is less than or equal to
	 */

	public static void main(String[] args) {

		int x = 10; // stores a int with a value of 10

		if (x == 10) {
			System.out.println("yes x == 10"); // if the number = 10 , it will
												// print out yes/true statement
		} else {
			System.out.println("no x != 10"); // if the number is not = to , it
												// print out no
		}
	}

}
